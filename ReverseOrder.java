// Print elements in Reverse Order

package com.javaarray;
public class ReverseOrder {

    public static void main(String[] args)
    {

        int[] arr={5,7,9,7,6,2,4,6};

        int len=arr.length;
        for(int i=len-1;i>=0;i--)

        {

                System.out.print(arr[i]);

        }


    }
}
